package visualizer;

public class Delay {
	private int delay;

	public Delay(int delay) {
		this.delay = delay;
	}

	/**
	 * @return the delay
	 */
	public int get() {
		return this.delay;
	}

	/**
	 * @param delay
	 *            the delay to set
	 */
	public void set(int delay) {
		this.delay = delay;
	}

}
